imprimirResultado([]).
imprimirResultado([C | Cauda]) :-
    write(C),
    write("\n"),
    imprimirResultado(Cauda).


maior_volume(Moeda,Ano):-
   aggregate_all(max(V),currency(Moeda,_,_,Ano,_,_,_,_,V,_), X),
    currency(Moeda,M,D,Ano,_,_,_,_,X,_),
    write(D),write(" de "),write(M), write(" de "), write(Ano), write(": R$ "), format('~2D',[X]).


maximo_hight(Moeda,Ano,X):-
    aggregate_all(max(Hight),currency(Moeda,_,_,Ano,_,Hight,_,_,_,_),X).


media_anual(Moeda,Ano,X):-
    aggregate_all(sum(Close),currency(Moeda,_,_,Ano,_,_,_,Close,_,_), Y),
    aggregate_all(count,currency(Moeda,_,_,Ano,_,_,_,_,_,_), Z),
    X is Y/Z.


media_mes(Coin, Year, Month, Media_BTC_Mes):-
    aggregate_all(sum(Close), currency(Coin, Month, _, Year, _, _, _, Close, _, _), X),
	aggregate_all(count, currency(Coin, Month, _, Year, _, _, _, _, _, _), Y),
    Media_BTC_Mes is X/Y.


media_periodo(Moeda, X):-
	aggregate_all(sum(Close),currency(Moeda,_,_,_,_,_,_,Close,_,_), Y),
    aggregate_all(count,currency(Moeda,_,_,_,_,_,_,_,_,_), Z),
	X is Y/Z.


refina_Lista([],[]).

refina_Lista([Cab|Cauda] , [Cab|Cauda2]):-
    not(member(Cab,Cauda)),
    refina_Lista(Cauda,Cauda2).

refina_Lista([_|Cauda], Lr):-
    refina_Lista(Cauda,Lr).

meses_com_fechamento_acima_de(X,Moeda):- %findall
    findall(Meses, ( currency(Moeda,Meses,_,_,_,_,_,Close,_,_), Close>X ), Lista_meses),
    refina_Lista(Lista_meses,Lr),
	imprimirResultado(Lr),
	!.
   

media_mensal(Moeda,Ano,Mes,Z):-
    aggregate_all(sum(Close), currency(Moeda,Mes,_,Ano,_,_,_,Close,_,_), Soma),
    aggregate_all(count, currency(Moeda,Mes,_,Ano,_,_,_,_,_,_), Qtde),
    Z is Soma/Qtde.
    
diferenca_entre_meses(Moeda,[Ano1|[Mes1|[Ano2|[Mes2|[]]]]]):-
    media_mensal(Moeda,Ano1,Mes1, X),
    media_mensal(Moeda,Ano2,Mes2, Y),
    format('Media ~w/~w: ~2f ($) ~n',[Mes2,Ano2,Y]),
    format('Media ~w/~w: ~2f ($) ~n',[Mes1,Ano1,X]),
    Dif is Y-X,
    format('Diferença: ~2f ($) ~n',[Dif]),
    Percentual is ((Y-X)/X)*100,
    format('Porcentagem: ~2f% ~n',[Percentual]).

